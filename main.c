/* USER CODE BEGIN 0 */
#define USB_IS_ACTIVE() (hUsbDeviceFS.dev_state == USBD_STATE_CONFIGURED)
#define CDC_TX_FREE() (((USBD_CDC_HandleTypeDef *)hUsbDeviceFS.pClassData)->TxState == 0)
#define CDC_RX_FREE() (((USBD_CDC_HandleTypeDef *)hUsbDeviceFS.pClassData)->RxState == 0)

#define ADC_SAMPLE_BUF_SIZE 512

uint32_t adcbuf[ADC_SAMPLE_BUF_SIZE];
uint8_t txbuf[ADC_SAMPLE_BUF_SIZE];
uint8_t rxbuf[CDC_DATA_FS_MAX_PACKET_SIZE];

uint8_t appstate = 0;

void vTaskTransmitSamples(void const * arguments)
{
  uint32_t i;
  while (1)
  {
    // wait conversion
    osSemaphoreWait(conversionWait, 0);

    if (USB_IS_ACTIVE())
    {
      // copy values to TX buffer
      for (i = 0; i < ADC_SAMPLE_BUF_SIZE; i++)
      {
        txbuf[i] = adcbuf[i] / 16;
      }

      // send
      USBD_CDC_SetTxBuffer(&hUsbDeviceFS, txbuf, ADC_SAMPLE_BUF_SIZE);
      USBD_CDC_TransmitPacket(&hUsbDeviceFS);
      while (USB_IS_ACTIVE() && !CDC_TX_FREE());
    }
  }
}

void vTaskCommandReceive(void const * arguments)
{
  while (1)
  {
    if (USB_IS_ACTIVE())
    {
      USBD_CDC_SetRxBuffer(&hUsbDeviceFS, rxbuf);
      USBD_CDC_ReceivePacket(&hUsbDeviceFS);
      while (USB_IS_ACTIVE() && !CDC_RX_FREE());

      // commands
      switch (rxbuf[0])
      {
      case 'b':
        // begin
        if (appstate == 0)
        {
          appstate = 1;
          HAL_GPIO_WritePin(GPIOC, GPIO_PIN_7, GPIO_PIN_SET);
          HAL_ADCEx_MultiModeStart_DMA(&hadc1, adcbuf, ADC_SAMPLE_BUF_SIZE);
        }
        break;
      case 'e':
        // end
        if (appstate == 1)
        {
          appstate = 0;
          HAL_GPIO_WritePin(GPIOC, GPIO_PIN_7, GPIO_PIN_RESET);
          HAL_ADCEx_MultiModeStop_DMA(&hadc1);
          // Discard TX(IN) buffer
          USBD_LL_DataInStage(&hUsbDeviceFS, CDC_IN_EP, 0);
        }
        break;
      }

      rxbuf[0] = 0;

      // indicate connection state
      HAL_GPIO_WritePin(GPIOC, GPIO_PIN_6, GPIO_PIN_SET);
    }
    else
    {
      HAL_GPIO_WritePin(GPIOC, GPIO_PIN_6, GPIO_PIN_RESET);
    }
    osDelay(100);
  }
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
  if (appstate == 1)
  {
    osSemaphoreRelease(conversionWait);
  }
}
